#include<iostream>

using namespace std;

int main(){
    int a,b,c,d;
    cin>>a;
    while(a>0){
     cin>>b;
     c=0;
      for(int i=400;i<=b;i+=400){
       c+=1;
       d=i;
      }
      if(b>d){
          c+=1;
      }
      if(b<400){
          c+=1;

      }
      cout<<c<<endl;
      a-=1;

    }
    return 0;
}
